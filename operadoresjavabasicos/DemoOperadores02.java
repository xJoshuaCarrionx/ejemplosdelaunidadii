package operadoresjavabasicos;
public class DemoOperadores02 {
	public static void main(String[] args) {
		// Operadores unarios
		int x=10;
		System.out.println("x="+x);
		x++; //Postincremento
		System.out.println("x="+x);
		//Postincremento, pero el valor incrementado
		System.out.println("x="+ x++);
		//se muestro en la siguiente sentencia
		System.out.println("x="+x);
	}
}
